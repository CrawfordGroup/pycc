def test_cisd_hessian_vs_reference():
    H = np.asarray(_ciwfn().hessian())
    assert np.max(np.abs(H - HESS_REF)) < 1e-6, np.max(np.abs(H - HESS_REF))


def test_cisd_hessian_symmetry_sum_rule():
    H = np.asarray(_ciwfn().hessian())
    natom = 4
    assert H.shape == (3 * natom, 3 * natom)
    assert np.max(np.abs(H - H.T)) < 1e-10
    assert np.max(np.abs(H.reshape(natom, 3, natom, 3).sum(axis=2))) < 1e-6
